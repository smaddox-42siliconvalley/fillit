#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int		i;
	int		len;
	char	*fresh;

	if (s == NULL)
		return (NULL);
	len = ft_strlen(s);
	while (s[len - 1] == ' ' || s[len - 1] == '\t' || s[len - 1] == '\n')
		len--;
	i = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n')
	{
		len--;
		i++;
	}
	if (len <= 0)
		len = 0;
	if (!(fresh = (char*)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	s = s + i;
	i = 0;
	while (i < len)
		fresh[i++] = *s++;
	fresh[i] = '\0';
	return (fresh);
}
